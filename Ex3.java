package Exercise;
//understand preorder and postorder , asks in interview .
public class Ex3 {
    public static void main(String[] args){
        int i =0;
        while(i++<10){
            System.out.println(i);
            
        }int j=0;
        while( ++j<10){
            System.out.println(j);

        }
    
    }
    
}
